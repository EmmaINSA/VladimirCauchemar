//===========================================================================================================================
//
// Objet extension de JFrame qui programme un des 3 graphiques : la synthese de Fourier
//
//===========================================================================================================================


import javax.swing.JFrame;

public class Synthesis extends JFrame{

	MainPanel mp;

		public Synthesis(){

//===========================================================================================================================
// Variables utiles
//===========================================================================================================================

			int width = 400;
			int height = 400;
			String title = "Synthese";

//===========================================================================================================================
// Parametrage
//===========================================================================================================================

			setSize(width,height);
			setTitle(title);
			setLocation(90,500);

			setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
		}

//===========================================================================================================================
// Methode pour rafraichir le Panel a chaque action de l'utilisateur
//===========================================================================================================================

		public void refresh(String instrument, double frequence){
			mp = new MainPanel(instrument, frequence);
			add(mp);
		}
}
